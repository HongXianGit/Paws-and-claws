        <hr>
        <p>IPOH: The Ipoh Society for the Prevention of Cruelty to Animals (ISPCA) is in the process of setting up of a shelter for stray dogs in Papan, near here.</p>
        <p>Homeless canines in the deserted town are often seen roaming near landfills, in desperate search of food; while many others end up as roadkill.</p>
        <p>If left unaddressed, the issue could also lead to an explosion in the stray dog population, as no spaying and neutering programme has been carried out in the area.</p>
        <p>In spite of the urgency, no land has been allocated for the project.</p>
        <p>Nevertheless, ISPCA president Ricky Soong is hopeful that the local government would grant a piece of land for the purpose.</p>
        <p>He said the shelter would help save the animals from suffering and give them the proper treatment and care they need.</p>
        <p>&quot;The shelter would also be used for the neutering or spaying of stray dogs,&quot; he said, adding that the facility would house strays caught by the local council.</p>
        <p>Soong said the ISPCA is working with other non-governmental organisations to make the proposed shelter a reality.</p>
        <p>&quot;All we need is half an acre of land and we can build a shelter that can take in at least 300 stray dogs.</p>
        <p>&quot;Construction and upkeep of the shelter would require (monetary) donations and supplies from the public.</p>
        <p>&quot;It is estimated that the shelter could cost up to RM60,000; while operational expenses would be around RM10,000 per month,&rdquo; he added.</p>
        <p>Soong said the ISPCA would also welcome volunteers to help out at the shelter once it is up and running.</p>
